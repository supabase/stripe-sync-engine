#!/usr/bin/env node
/**
 * Lightweight: generates src/versions.ts and src/deprecatedPaths.ts from the
 * single bundled spec in oas/ + the CDN manifest.
 *
 * BUNDLED_API_VERSION    — the single *.json file in oas/
 * SUPPORTED_API_VERSIONS — same as bundled (lightweight fallback)
 * GLOBALLY_DEPRECATED_PATHS — union of deprecated GET paths across ALL CDN specs
 *
 * The deprecated-paths scan fetches every spec listed in the CDN manifest.
 *
 * Run directly:  node scripts/generate-versions.mjs
 * Run via build: pnpm --filter @stripe/sync-openapi build
 */
import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const oasDir = join(__dirname, '..', 'oas')
const outFile = join(__dirname, '..', 'src', 'versions.ts')
const deprecatedFile = join(__dirname, '..', 'src', 'deprecatedPaths.ts')

const CDN_BASE = process.env.STRIPE_SPEC_CDN_BASE_URL ?? 'https://stripe-sync.dev/stripe-api-specs'

const bundledFiles = readdirSync(oasDir).filter(
  (f) => f.endsWith('.json') && f !== 'manifest.json' && f !== 'index.html'
)
if (bundledFiles.length !== 1) {
  console.error(
    `Expected exactly 1 bundled spec in oas/, found: ${bundledFiles.join(', ') || 'none'}`
  )
  process.exit(1)
}
const bundled = bundledFiles[0].replace(/\.json$/, '')

mkdirSync(dirname(outFile), { recursive: true })
writeFileSync(
  outFile,
  `// Generated by scripts/generate-versions.mjs — do not edit manually.
// For the full list of all published Stripe API versions, run:
//   node scripts/generate-specs.mjs <outputDir>

/** The single Stripe API spec bundled in this package (served without network). */
export const BUNDLED_API_VERSION = '${bundled}'

/** All Stripe API versions published by Stripe, newest first. */
export const SUPPORTED_API_VERSIONS = [
  '${bundled}',
] as const satisfies readonly string[]
`
)

const DEPRECATED_DESCRIPTION_RE = /^\s*<p>\s*\[Deprecated\]/i

function scanDeprecatedGetPaths(spec, into) {
  for (const [apiPath, pathItem] of Object.entries(spec.paths ?? {})) {
    const getOp = pathItem?.get
    if (!getOp?.responses) continue
    if (
      getOp.deprecated === true ||
      (getOp.description && DEPRECATED_DESCRIPTION_RE.test(getOp.description))
    ) {
      into.add(apiPath)
    }
  }
}

const allDeprecated = new Set()

// Always include the bundled spec (available offline).
try {
  const spec = JSON.parse(readFileSync(join(oasDir, `${bundled}.json`), 'utf8'))
  scanDeprecatedGetPaths(spec, allDeprecated)
} catch (err) {
  console.error(`Warning: could not scan bundled spec: ${err.message}`)
}

// Fetch every spec from the CDN and scan for deprecated paths.
try {
  console.error(`Fetching CDN manifest from ${CDN_BASE}/manifest.json ...`)
  const manifestRes = await fetch(`${CDN_BASE}/manifest.json`)
  if (!manifestRes.ok) throw new Error(`HTTP ${manifestRes.status}`)
  const manifest = await manifestRes.json()
  const entries = Object.entries(manifest)
  console.error(`Scanning ${entries.length} specs for deprecated paths ...`)

  // Fetch specs in parallel (batched to avoid overwhelming the CDN).
  const BATCH_SIZE = 10
  for (let i = 0; i < entries.length; i += BATCH_SIZE) {
    const batch = entries.slice(i, i + BATCH_SIZE)
    const results = await Promise.allSettled(
      batch.map(async ([version, filename]) => {
        const res = await fetch(`${CDN_BASE}/${filename}`)
        if (!res.ok) {
          console.error(`  Warning: ${version} HTTP ${res.status}, skipping`)
          return
        }
        const spec = await res.json()
        scanDeprecatedGetPaths(spec, allDeprecated)
      })
    )
    for (const r of results) {
      if (r.status === 'rejected') {
        console.error(`  Warning: fetch failed: ${r.reason?.message ?? r.reason}`)
      }
    }
  }
} catch (err) {
  console.error(`Warning: CDN fetch failed, using bundled spec only: ${err.message}`)
}

const deprecatedLines = [...allDeprecated]
  .sort()
  .map((p) => `  '${p}',`)
  .join('\n')
writeFileSync(
  deprecatedFile,
  `// Generated by scripts/generate-versions.mjs — do not edit manually.
// Union of all deprecated GET paths across all known Stripe API versions.
// Generated by scanning the bundled spec plus every spec published in the CDN.
export const DEPRECATED_PATHS_BUNDLED_VERSION = '${bundled}'

export const GLOBALLY_DEPRECATED_PATHS: ReadonlySet<string> = new Set([
${deprecatedLines}
])
`
)

console.log(`Bundled: ${bundled}`)
console.log(`Deprecated paths: ${allDeprecated.size}`)
